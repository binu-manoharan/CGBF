package org.binu.data;

/**
 * Enum for Rotation of the block
 * <ul>
 * <li>HORIZONTAL 1 -> 4</li>
 * <li>VERTCIAL 1 below 4</li>
 * <li>HORIZONTAL_REVERSED 4 -> 1</li>
 * <li>VERTICAL_REVERSED 4 below 1</li>
 * </ul>
 */
public enum Orientation {
    HORIZONTAL,
    VERTICAL,
    HORIZONTAL_REVERSED,
    VERTICAL_REVERSED
}
